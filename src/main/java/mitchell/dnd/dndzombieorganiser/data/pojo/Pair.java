package mitchell.dnd.dndzombieorganiser.data.pojo;

public class Pair {

    private int A;
    private int B;

    public Pair(int a, int b) {
        A = a;
        B = b;
    }

    public Pair() {}

    public int getA() {
        return A;
    }

    public int getB() {
        return B;
    }

    public void setA(int a) {
        A = a;
    }

    public void setB(int b) {
        B = b;
    }
}
